Bower :

Install:
	npm install -g bower	

Installing package methods:
	// installs the project dependencies listed in bower.json
	bower install
	// registered package
	bower install jquery
	// GitHub shorthand
	bower install desandro/masonry
	// Git endpoint
	bower install git://github.com/user/package.git
	// URL
	bower install http://example.com/script.js


Start Bower in a project:
	bower init
Install a package in the project
	bower install <package>
	bower install <package> --save

Bower packages :
	https://bower.io/search/

	bower list
	bower list --paths
	bower uninstall jquery
	bower install jquery#1.9.2	


Bower search available versions for a package:
	bower info font-awesome
Bower install specific versions
	bower install font-awesome#4.7.0
